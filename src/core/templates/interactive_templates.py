                "## 🔗 延伸活动\n"
                "辩论后的延伸活动：\n\n"
                "### 反思写作\n"
                "- 写作题目：[反思写作的题目]\n"
                "- 写作要求：[写作的具体要求]\n"
                "- 字数要求：[字数要求]\n\n"
                "### 观点整合\n"
                "- 整合任务：[如何整合双方观点]\n"
                "- 呈现方式：[如何呈现整合结果]\n\n"
                "## 📚 参考资源\n"
                "列出使用的参考资料，包括来源文档和页码\n\n"
                "---\n\n"
                "**以下部分必须在所有教案中包含：**\n\n"
